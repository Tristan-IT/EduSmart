# Output format untuk TypeScript
def parse_data(data_str, kelas, jurusan, semester):
    lines = [line.strip() for line in data_str.strip().split('\n') if line.strip()]
    result = []
    for line in lines:
        parts = line.split('|')
        if len(parts) == 3:
            nama, nisn, nilai = parts
            result.append(f"  {{ nisn: '{nisn}', nama: '{nama}', kelas: {kelas}, jurusan: '{jurusan}', semester: {semester}, nilai_sts: {nilai} }},")
    return '\n'.join(result)

